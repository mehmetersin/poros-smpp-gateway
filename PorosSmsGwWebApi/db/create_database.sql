 CREATE TABLE "MESSAGE_DETAIL" 
   (	"ID" NUMBER, 
	"TEXT" VARCHAR2(160), 
	"ORIGINATOR" VARCHAR2(11), 
	"COMPLETED" NUMBER(*,0) DEFAULT 0 NOT NULL ENABLE, 
	"STARTDATE" TIMESTAMP (6), 
	"USERID" NUMBER, 
	 CONSTRAINT "MESSAGE_DETAIL_PK" PRIMARY KEY ("ID"));


