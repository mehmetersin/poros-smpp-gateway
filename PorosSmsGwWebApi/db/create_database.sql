	"DESTINATION" VARCHAR2(100) NOT NULL ENABLE, 
	"SENDINGSTATUS" NUMBER DEFAULT 1 NOT NULL ENABLE, 
	"TRANSACTIONID" VARCHAR2(100), 
	"RETRYCOUNT" NUMBER DEFAULT 0 NOT NULL ENABLE, 
	"STATE" NUMBER DEFAULT 0 NOT NULL ENABLE, 
	"PROCESSINGDATE" TIMESTAMP (6), 
	"DELIVERYDATE" TIMESTAMP (6), 
